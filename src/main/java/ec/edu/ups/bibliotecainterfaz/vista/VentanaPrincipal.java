  private void ponerFondoMadera() {
       
        java.awt.Container diseñoOriginal = this.getContentPane();
        
        javax.swing.JPanel panelConFondo = new javax.swing.JPanel() {
            @Override
            protected void paintComponent(java.awt.Graphics g) {
                super.paintComponent(g);
                try {
                    String rutaPrincipal = "C:\\Users\\HP\\Documents\\NetBeansProjects\\BibliotecaInterfaz\\src\\main\\java\\ec\\edu\\ups\\bibliotecainterfaz\\imagenes\\fondo2.jpg"; 
                    java.awt.Image img = new javax.swing.ImageIcon(rutaPrincipal).getImage();
                    
                    if (img.getWidth(null) > 0) g.drawImage(img, 0, 0, getWidth(), getHeight(), this);
                    else {
                        img = new javax.swing.ImageIcon(rutaPrincipal + ".jpeg").getImage();
                        if (img.getWidth(null) > 0) g.drawImage(img, 0, 0, getWidth(), getHeight(), this);
                        else { g.setColor(java.awt.Color.RED); g.fillRect(0, 0, getWidth(), getHeight()); }
                    }
                } catch (Exception e) {}
            }
        };
        
        panelConFondo.setLayout(new java.awt.BorderLayout());
        
        
        if (diseñoOriginal instanceof javax.swing.JComponent) ((javax.swing.JComponent) diseñoOriginal).setOpaque(false);
        for (java.awt.Component hijo : diseñoOriginal.getComponents()) {
            if (hijo instanceof javax.swing.JComponent) ((javax.swing.JComponent) hijo).setOpaque(false);
        }
        
        panelConFondo.add(diseñoOriginal);
        this.setContentPane(panelConFondo);

        
        javax.swing.JButton btnUsuariosCentro = new javax.swing.JButton("Usuarios");
        javax.swing.JButton btnLibrosCentro = new javax.swing.JButton("Libros");
        javax.swing.JButton btnPrestamosCentro = new javax.swing.JButton("Préstamos");
        javax.swing.JButton btnDevolucionesCentro = new javax.swing.JButton("Devoluciones");
        
        
        btnUsuariosCentro.addActionListener(e -> btnUsuarios.doClick());
        btnLibrosCentro.addActionListener(e -> btnLibros.doClick());
        btnPrestamosCentro.addActionListener(e -> btnPrestamos.doClick());
        btnDevolucionesCentro.addActionListener(e -> jMenuItem4.doClick());

        
        String rutaBase = "C:\\Users\\HP\\Documents\\NetBeansProjects\\BibliotecaInterfaz\\src\\main\\java\\ec\\edu\\ups\\bibliotecainterfaz\\imagenes\\";
        
      
        java.util.function.BiConsumer<javax.swing.JButton, String> formatearBoton = (btn, archivo) -> {
            try {
                java.awt.Image iconImg = new javax.swing.ImageIcon(rutaBase + archivo).getImage();
                if(iconImg != null && iconImg.getWidth(null) > 0) {
                    btn.setIcon(new javax.swing.ImageIcon(iconImg.getScaledInstance(120, 120, java.awt.Image.SCALE_SMOOTH)));
                }
            } catch(Exception e){}
            
            btn.setVerticalTextPosition(javax.swing.SwingConstants.BOTTOM); 
            btn.setHorizontalTextPosition(javax.swing.SwingConstants.CENTER); 
            btn.setFont(new java.awt.Font("Segoe UI", 1, 20)); 
            btn.setForeground(java.awt.Color.WHITE); 
            btn.setContentAreaFilled(false); 
            btn.setBorderPainted(false); 
            btn.setFocusPainted(false);
            btn.setCursor(new java.awt.Cursor(java.awt.Cursor.HAND_CURSOR)); 
        };

        
        formatearBoton.accept(btnUsuariosCentro, "usuarios.png");
        formatearBoton.accept(btnLibrosCentro, "libros.png");
        formatearBoton.accept(btnPrestamosCentro, "prestamos.png");
        formatearBoton.accept(btnDevolucionesCentro, "devoluciones.png");

       
        javax.swing.JPanel pnlCentro = new javax.swing.JPanel(new java.awt.FlowLayout(java.awt.FlowLayout.CENTER, 60, 20));
        pnlCentro.setOpaque(false); 
        pnlCentro.add(btnUsuariosCentro);
        pnlCentro.add(btnLibrosCentro);
        pnlCentro.add(btnPrestamosCentro);
        pnlCentro.add(btnDevolucionesCentro);

        
        desktopContenedor.add(pnlCentro);

        
        desktopContenedor.addComponentListener(new java.awt.event.ComponentAdapter() {
            @Override
            public void componentResized(java.awt.event.ComponentEvent evt) {
                pnlCentro.setSize(desktopContenedor.getWidth(), 250);
                pnlCentro.setLocation(0, (desktopContenedor.getHeight() - 250) / 2);
            }
        });
        
        this.revalidate();
        this.repaint();
    }

